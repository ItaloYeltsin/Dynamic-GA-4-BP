package jmetal.util.dga4nrp;

public class Bug {
	int id;
	int votes;
	double priority;
	double serity;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getVotes() {
		return votes;
	}
	public void setVotes(int votes) {
		this.votes = votes;
	}
	public double getPriority() {
		return priority;
	}
	public void setPriority(double priority) {
		this.priority = priority;
	}
	public double getSerity() {
		return serity;
	}
	public void setSerity(double serity) {
		this.serity = serity;
	}
	
	
}
